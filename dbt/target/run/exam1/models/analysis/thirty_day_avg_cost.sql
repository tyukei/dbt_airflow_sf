
  create or replace   view KEITA_NAKATA_DB.AIRFLOW_DBT.thirty_day_avg_cost
  
   as (
    SELECT
  BOOKING_DATE,
  HOTEL,
  COST,
  AVG(COST) OVER (
    ORDER BY BOOKING_DATE ROWS BETWEEN 29 PRECEDING AND CURRENT ROW
  ) as "30_DAY_AVG_COST",
  COST -   AVG(COST) OVER (
    ORDER BY BOOKING_DATE ROWS BETWEEN 29 PRECEDING AND CURRENT ROW
  ) as "DIFF_BTW_ACTUAL_AVG"
FROM KEITA_NAKATA_DB.AIRFLOW_DBT.prepped_data
  );

